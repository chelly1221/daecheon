/** Props shared by the two ways an icon is consumed. */
export interface IconProps {
  name: IconName;
  /** Rendered box in px (width = height). Match the font-size the glyph used. */
  size?: number;
  /** Fill colour; defaults to `currentColor` so it inherits like text did. */
  color?: string;
  style?: CSSProperties;
}

/**
 * Inline-SVG icon. Drop-in replacement for the old
 * `<span style="font-family:'Material Symbols Rounded';font-size:Npx;color:C">name</span>`
 * pattern — pass the ligature as `name`, the font-size as `size`, the colour as `color`.
 */
export function Icon({ name, size = 24, color, style }: IconProps) {
  return (
    <svg
      viewBox="0 -960 960 960"
      width={size}
      height={size}
      aria-hidden="true"
      focusable="false"
      style={{ fill: color ?? 'currentColor', display: 'block', flexShrink: 0, ...style }}
    >
      <path d={ICON_PATHS[name]} />
    </svg>
  );
}

/**
 * The same glyph as a raw markup string, for the handful of sinks that inject
 * HTML rather than render React — e.g. Leaflet's `divIcon` map markers.
 */
export function iconSvg(name: IconName, size: number, color: string): string {
  return (
    `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 -960 960 960" ` +
    `width="${size}" height="${size}" fill="${color}"><path d="${ICON_PATHS[name]}"/></svg>`
  );
}

