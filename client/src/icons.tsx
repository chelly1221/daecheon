/** Weather look-buckets, mirroring the old `weatherEmoji` thresholds 1:1. */
export type WeatherKind =
  | 'clear'
  | 'mostlyClear'
  | 'partly'
  | 'cloudy'
  | 'fog'
  | 'showers'
  | 'rain'
  | 'snow'
  | 'thunder';

/** WMO weather_code → look bucket (same cut points as the retired weatherEmoji). */
export function weatherKind(code: number | null | undefined): WeatherKind {
  if (code == null) return 'partly';
  if (code === 0) return 'clear';
  if (code <= 2) return 'mostlyClear';
  if (code === 3) return 'cloudy';
  if (code <= 48) return 'fog';
  if (code <= 57) return 'showers';
  if (code <= 67) return 'rain';
  if (code <= 77) return 'snow';
  if (code <= 82) return 'showers';
  return 'thunder';
}

const SUN = '#F6A623';
const CLOUD = '#AFC4D6';
const RAIN = '#4C9AE0';
const SNOW = '#83B4E0';
const FOG = '#9DB4C8';

// A fluffy cloud built from primitives (no path guesswork): spans x≈4.5–20,
// bottom flat at y≈18, horizontally centred. `dy` nudges it up for the kinds
// that hang precipitation beneath it.
function Cloud({ dy = 0 }: { dy?: number }) {
  return (
    <g fill={CLOUD} transform={dy ? `translate(0 ${dy})` : undefined}>
      <circle cx="8.5" cy="14" r="3.7" />
      <circle cx="16" cy="14" r="3.7" />
      <circle cx="12.5" cy="11" r="4.7" />
      <rect x="8" y="13" width="9" height="5" rx="2.4" />
    </g>
  );
}

// Full sun, centred — used on its own for a clear sky.
